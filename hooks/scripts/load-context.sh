#!/bin/bash
# ============================================================
# SessionStart Hook: 이전 분석 리포트 요약을 세션 컨텍스트에 주입
# ============================================================
# 동작:
#   - ./reports/ 디렉토리에서 가장 최신 analysis-*.md 파일을 찾아
#   - 논점 정의(1번) + 추천 시책(6번) 섹션만 추출하여 컨텍스트로 출력
#   - Claude Code 세션 시작 시 이전 분석 맥락을 자동으로 인지
# 
# stdout 에 출력한 내용이 세션 컨텍스트에 추가됨
# ============================================================

set -euo pipefail

REPORTS_DIR="./reports"

# 리포트 디렉토리 없으면 조용히 종료 (세션 방해 방지)
[ ! -d "$REPORTS_DIR" ] && exit 0

# 최신 리포트 파일 찾기
LATEST=$(ls -t "$REPORTS_DIR"/analysis-*.md 2>/dev/null | head -1)

# 리포트 없으면 조용히 종료
[ -z "$LATEST" ] && exit 0

# 파일 생성 시각 (KST 기준 포맷)
TIMESTAMP=$(basename "$LATEST" | sed -E 's/analysis-([0-9]{8})-([0-9]{4})\.md/\1 \2/')

echo "===== 이전 분석 리포트 컨텍스트 ====="
echo "파일: $LATEST"
echo "시각: $TIMESTAMP (KST)"
echo ""

# 논점 정의 섹션 추출 (## 1. 논점 정의 ~ 다음 ## 까지)
echo "### 이전 논점 정의"
awk '/^## 1\. 논점 정의/,/^## 2\./' "$LATEST" | sed '/^## 2\./d'
echo ""

# 추천 시책 섹션 추출 (## 6. 추천 ~ 다음 ## 까지)
echo "### 이전 추천 시책"
awk '/^## 6\. 추천/,/^## 7\./' "$LATEST" | sed '/^## 7\./d'
echo ""

echo "===== 컨텍스트 종료 ====="
echo ""
echo "이전 분석을 참고하되, 필요 시 재검토하여 새 논점을 제시할 것."
